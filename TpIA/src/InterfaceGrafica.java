    private void btn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn1MouseClicked
        // TODO add your handling code here:
        tbusca=1;
        xtexto.setText("Iniciando busca em largura");
        int estadoInicial[][] = {{4, 1, 3},
        {0, 2, 6},
        {7, 5, 8}};//{{1,2,3},{4,5,0},{7,8,6}};//{{7,2,4},{5,0,6},{8,3,1}};
        if (pegadadosTela() != null) {
            estadoInicial = pegadadosTela();
        }
        Estado e = new Estado(estadoInicial);

        No noInicial = new No(e, 0, -1);
        BuscaLargura busca = new BuscaLargura();
        BuscaLargura.setNumerodeNos(0);
        cont = 0;
        if (busca.Busca(noInicial, Integer.valueOf(profundidade.getText()))) {
            jButton1.setEnabled(true);
            xtexto.setText("Busca em Largura Realizada Com Sucesso");
            
        } else {
            xtexto.setText("busca não Enscontrou solução tente aumentar a profundidade");
        }
        nnos.setText(String.valueOf(BuscaLargura.numerodeNos));

    }//GEN-LAST:event_btn1MouseClicked
