    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(tbusca==1){
        if (jButton1.isEnabled()) {
            prenchertela(BuscaLargura.caminho.get(cont).getEstado().getMatriz());
        }
        if (BuscaLargura.caminho.get(cont).getEstado().Objetivo(BuscaLargura.caminho.get(cont).getEstado().getMatriz())) {
            jButton1.setEnabled(false);
        }
        }else if(tbusca==3){
            if(jButton1.isEnabled()){
                prenchertela(AEstrela.Caminho.get(cont).getEstado().getMatriz());
            }
            if(AEstrela.Caminho.get(cont).getEstado().Objetivo(AEstrela.Caminho.get(cont).getEstado().getMatriz())){
                jButton1.setEnabled(false);
            }
        }
        else if(tbusca==4){
            if(jButton1.isEnabled()){
                prenchertela(BuscaGulosa.Caminho.get(cont).getEstado().getMatriz());
            }
            if(BuscaGulosa.Caminho.get(cont).getEstado().Objetivo(BuscaGulosa.Caminho.get(cont).getEstado().getMatriz())){
                jButton1.setEnabled(false);
            }
        }
        else{
            nnos.setText(String.valueOf(BuscaProfundidade.NumerodeNos));
            if (jButton1.isEnabled()) {
            prenchertela(BuscaProfundidade.caminho.get(cont).getEstado().getMatriz());
        }
        if (BuscaProfundidade.caminho.get(cont).getEstado().Objetivo(BuscaProfundidade.caminho.get(cont).getEstado().getMatriz())) {
            jButton1.setEnabled(false);
            
        }
       
        }
        
         cont++;          

    }//GEN-LAST:event_jButton1MouseClicked

