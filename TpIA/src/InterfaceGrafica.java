    public void prenchertela(int mat[][]) {
        t1.setText(String.valueOf(mat[0][0]));
        t2.setText(String.valueOf(mat[0][1]));
        t3.setText(String.valueOf(mat[0][2]));
        t4.setText(String.valueOf(mat[1][0]));
        t5.setText(String.valueOf(mat[1][1]));
        t6.setText(String.valueOf(mat[1][2]));
        t7.setText(String.valueOf(mat[2][0]));
        t8.setText(String.valueOf(mat[2][1]));
        t9.setText(String.valueOf(mat[2][2]));

    }

    public int[][] pegadadosTela() {
        int[][] mat = new int[3][3];
        if ((!t1.getText().isEmpty()) && (!t2.getText().isEmpty())
                && (!t3.getText().isEmpty()) && (!t4.getText().isEmpty())
                && (!t5.getText().isEmpty()) && (!t6.getText().isEmpty())
                && (!t7.getText().isEmpty()) && (!t8.getText().isEmpty())
                && (!t9.getText().isEmpty())) {
            mat[0][0] = Integer.valueOf(t1.getText());
            mat[0][1] = Integer.valueOf(t2.getText());
            mat[0][2] = Integer.valueOf(t3.getText());
            mat[1][0] = Integer.valueOf(t4.getText());
            mat[1][1] = Integer.valueOf(t5.getText());
            mat[1][2] = Integer.valueOf(t6.getText());
            mat[2][0] = Integer.valueOf(t7.getText());
            mat[2][1] = Integer.valueOf(t8.getText());
            mat[2][2] = Integer.valueOf(t9.getText());
            return mat;

        }

        return null;
    }

